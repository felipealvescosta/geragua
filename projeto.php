            <p>
            <h2>INTRODUÇÃO</h2>
A água é um bem essencial para a vida na terra, e uma das maiores riquezas da humanidade. Desta forma, o uso racional desse liquido tão precioso é indispensável para a conservação da vida no nosso planeta e a saúde do homem. Muitos lugares estão sofrendo com a falta d’agua por conta da uso irracional desse liquido. Assim, o GERAGUA foi desenvolvido para auxiliar no consumo e utilização da agua no dia-a-dia da comunidade, exibindo relatórios para acompanhamento e racionalização de água. 
</p>              
<p><h2>METODOLOGIA</h2>

No desenvolvimento do projeto, foi observado o desperdício de agua em diversos ambientes bem como em outros lugares na comunidade, desperdício desordenado desse bem. Assim, foi construído um sistema web utilizando HTML5, CSS3, PHP, servidor Apache® e Banco de Dados Oracle® MySQL 5.5. Foi criado um algoritmo em C para a plataforma Arduíno® juntamente com os seus componentes de hardware, esse sistema gera relatórios de acordo com a utilização da agua em uma residência ou caixa de distribuição, auxiliando ao indivíduo gerir com maior responsabilidade seus recursos hídricos.
</p>
<p><h2>RESULTADOS</h2> 
  
Os resultados estão relacionados com o melhor entendimento e racionalização dos recursos hídricos da residência. Bem como o auxílio através de gráficos e dicas para uma melhorar na conservação e reeducação domiciliar para a redução de desperdícios de agua no lar. Após coletados os dados referentes ao consumo de agua em uma residência e concluída a análise dos dados, são emitidos resultados expressos em forma de gráficos, os quais foram publicados em um site através de funções PHP e consultas ao banco de dados MySQL, para serem disponibilizados abertamente na internet, formando uma rede de registro e armazenamento de dados de consumo confiáveis em uma aplicação web de fácil acesso e entendimento.
</p>
<p><h2>CONSIDERAÇÕES FINAIS</h2> 

Os dados coletados pelo GERAGUA forneceram resultados sobre as diversas características, como, horas de maior consumo de água, menor consumo por hora, média de consumo, relatório diário, semanal e mensal do consumo médio desses períodos. Pedra Branca-CE, acompanhadas remotamente em tempo real por uma aplicação desenvolvida com tecnologias para a internet, facilitando a tomada de decisões referentes ao fator clima e alocação de recursos hidráulicos. 
</p>
